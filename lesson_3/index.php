Выведите в цикле значения массива,
чтобы результат был таким:
 Московская область:    Москва, Зеленоград, Клин
 Ленинградская область:    Санкт-Петербург, Всеволожск, Павловск, Кронштадт
 Рязанская область   …
 (названия городов можно найти на maps.yandex.ru)
-->

<?

$area_city = [
    'Московская область:' => ['Москва', 'Зеленоград', 'Клин'],
    'Ленинградская область:' => ['Санкт-Петербург', 'Всеволожск', 'Павловск', 'Кронштадт'],
    'Рязанская область:'=>['Касимов','Ряжск','РЯзань']
];

function displayCity($ar)
{
    if (!is_array($ar)) { //проверяет является ли массивом
        return print "ошибка '{$ar}'"; //print — Выводит строку
    }
    foreach ($ar as $key => $value) {
        echo $key . '<br>';
        for ($i = 0; $i < $arrLength = count($ar[$key]); $i++) {
            if ($i == $arrLength - 1) {         //проверяет последний ли обьект точка если последний
                echo $ar[$key][$i] . '.' . '<br>';
            } else {
                echo $ar[$key][$i] . ', '; //запятая если нет
            }
        }
    }
}

displayCity($area_city);

echo '<hr>';
