<?
$b = 0;
do {
    if ($b == 0) {
        echo $b . ' – это ноль.' . '<br>';
        $b++;
    } elseif ($b % 2 != 0) {
        echo $b . ' – нечетное число.' . '<br>';
        $b++;
    } else {
        echo $b. ' – четное число.' . '<br>';
        $b++;
    }
